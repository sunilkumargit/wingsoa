
namespace Wing.Mvc.Controls
{
    public enum CssTextDecoration
    {
        /// <summary>
        /// Defines a normal text. This is default
        /// </summary>
        None,

        /// <summary>
        /// Defines a line below the text
        /// </summary>
        Underline,

        /// <summary>
        /// Defines a line above the text
        /// </summary>
        Overline,

        /// <summary>
        /// Defines a line through the text
        /// </summary>
        LineThrough
    }
}
